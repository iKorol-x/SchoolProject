package com.skd.domain;

public class Information {
    private Integer id;
    private String name;
    private Integer age;
    private Double basesalary;
    private Integer workyear;
    private Integer attend_day;
    private Double attend_rate;
    private Double allowance;
    private Double salary;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Double getBasesalary() {
        return basesalary;
    }

    public void setBasesalary(Double basesalary) {
        this.basesalary = basesalary;
    }

    public Integer getWorkyear() {
        return workyear;
    }

    public void setWorkyear(Integer workyear) {
        this.workyear = workyear;
    }

    public Integer getAttend_day() {
        return attend_day;
    }

    public void setAttend_day(Integer attend_day) {
        this.attend_day = attend_day;
    }

    public Double getAttend_rate() {
        return attend_rate;
    }

    public void setAttend_rate(Double attend_rate) {
        this.attend_rate = attend_rate;
    }

    public Double getAllowance() {
        return allowance;
    }

    public void setAllowance(Double allowance) {
        this.allowance = allowance;
    }

    public Double getSalary() {
        return salary;
    }

    public void setSalary(Double salary) {
        this.salary = salary;
    }
}
